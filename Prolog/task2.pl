:- encoding(utf8).

сравнить(X, Y) :-
    name(X, L),
    name(Y, M),
    меньше_или_равно(L, M).

меньше_или_равно([], _).

меньше_или_равно([L|_], [M|_]) :-
    L < M.

меньше_или_равно([H|T1], [H|T2]) :-
    меньше_или_равно(T1, T2).


вывести_меньшее(X, Y) :-
    сравнить(X, Y),
    write('Меньшее слово: '),
    write(X), nl.

вывести_меньшее(X, Y) :-
    \+ сравнить(X, Y),
    write('Меньшее слово: '),
    write(Y), nl.